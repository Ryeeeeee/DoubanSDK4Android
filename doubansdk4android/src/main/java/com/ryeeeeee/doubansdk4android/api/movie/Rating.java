package com.ryeeeeee.doubansdk4android.api.movie;

/**
 * @author Ryeeeeee
 * @since 2015-02-24
 */
public class Rating {
    /** 最低评分 */
    private int min;
    /** 最高评分 */
    private int max;
    /** 评分 */
    private float average;
    /** 评星数 */
    private int stars;

    public Rating(int min, int max, float average, int stars) {
        this.min = min;
        this.max = max;
        this.average = average;
        this.stars = stars;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public float getAverage() {
        return average;
    }

    public void setAverage(float average) {
        this.average = average;
    }

    public int getStars() {
        return stars;
    }

    public void setStars(int stars) {
        this.stars = stars;
    }

    @Override
    public String toString() {
        return "Rating{" +
                "min=" + min +
                ", max=" + max +
                ", average=" + average +
                ", stars=" + stars +
                '}';
    }
}
